def hu7_from_img(img_u8: np.ndarray) -> np.ndarray:
    m = cv2.moments(img_u8, binaryImage=True)
    hu = cv2.HuMoments(m).reshape(7).astype(np.float32)
    return hu

def standardize(x7: np.ndarray, mean7: np.ndarray, std7: np.ndarray) -> np.ndarray:
    std7 = std7.copy()
    std7[std7 == 0] = 1.0
    return ((x7 - mean7) / std7).astype(np.float32)

def main():
    # A) 28x28 matrisi numpy array yap
    img = np.array(IMG_28x28, dtype=np.uint8)
    if img.shape != (28, 28):
        raise ValueError(f"IMG_28x28 shape yanlış: {img.shape}, (28,28) olmalı")

    # B) Eğitimde kaydedilen mean/std yükle
    mean = np.load("hu_mean.npy").astype(np.float32)
    std  = np.load("hu_std.npy").astype(np.float32)

    # C) HuMoments çıkar ve standardize et
    hu = hu7_from_img(img)
    hu_std = standardize(hu, mean, std)

    print("Hu7:", hu)
    print("Hu7 standardized:", hu_std)

    # D) Modeli yükle ve tahmin yap
    model = tf.keras.models.load_model("mlp_mnist_model.h5", compile=False)
    x = hu_std.reshape(1, 7)
    y = model.predict(x, verbose=0)[0]  # (10,)

    print("\nOutput scores:")
    for i, v in enumerate(y):
        print(f"{i}: {v:.6f}")

    pred = int(np.argmax(y))
    print("\nPredicted digit:", pred)

if __name__ == "__main__":
    main()
